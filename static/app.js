function renderMonitoring() {
  const summary = document.getElementById("monitoringSummary");
  const stats = document.getElementById("monitoringStats");
  const alerts = document.getElementById("monitoringAlerts");
  if (!summary || !stats || !alerts || !state.monitoring) return;
  if (state.monitoring.status === "not_run") {
    summary.textContent = "아직 모니터링 기준선이 없습니다.";
    stats.innerHTML = "";
    alerts.innerHTML = "";
    return;
  }
  const counts = state.monitoring.counts || {};
  const changes = state.monitoring.changes || {};
  const alertRows = state.monitoring.alerts || [];
  const fallbackRows = state.monitoring.top_attention || [];
  const cards = alertRows.length
    ? alertRows.slice(0, 6).map((row) => monitoringCard(row, row.severity === "high" ? "High" : "Watch")).join("")
    : fallbackRows.slice(0, 6).map((row) => monitoringCard(row, "기준선")).join("");
  summary.textContent = state.monitoring.summary || "";
  stats.innerHTML = [
    `즉시 검토 ${counts["즉시 검토"] ?? 0}`,
    `심층 실사 ${counts["심층 실사"] ?? 0}`,
    `고위험 옵션 ${counts["고위험 옵션"] ?? 0}`,
    `알림 ${alertRows.length}`,
    `등급 이동 ${(changes.group_changes || []).length}`,
    `공시 변화 ${(changes.latest_filing_changes || []).length}`,
  ]
    .map((text) => `<span class="meta-pill">${escapeHtml(text)}</span>`)
    .join("");
  alerts.innerHTML = cards || `<p class="note">표시할 변화가 없습니다.</p>`;
  bindMonitoringCards();
}

