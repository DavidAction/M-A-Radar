function signalCountValue(signalCounts = {}, category) {
  const row = signalCounts[category];
  if (!row || typeof row !== "object" || Array.isArray(row)) return 0;
  return Object.values(row).reduce((sum, value) => sum + (Number(value) || 0), 0);
}

function topSignalLabels(signalCounts = {}, category, limit = 3) {
  const row = signalCounts[category];
  if (!row || typeof row !== "object" || Array.isArray(row)) return [];
  return Object.entries(row)
    .sort((a, b) => (Number(b[1]) || 0) - (Number(a[1]) || 0))
    .slice(0, limit)
    .map(([label, count]) => `${label} ${count}`);
}

function normalizeEllipsis(text = "") {
  return String(text || "")
    .replace(/(?:\.\s*){4,}/g, "...")
    .replace(/\.{4,}/g, "...")
    .replace(/^\.{2,}\s*/, "...")
    .trim();
}

function appendEllipsis(text = "") {
  const cleaned = normalizeEllipsis(text).replace(/[.\s]+$/g, "").trim();
  return cleaned ? `${cleaned}...` : "";
}

function keywordSummary(values = [], limit = 2, empty = "특이신호 없음") {
  const clean = uniqueValues(values).filter(Boolean);
  if (!clean.length) return empty;
  const shown = clean.slice(0, limit).join(" · ");
  const hidden = clean.length - limit;
  return hidden > 0 ? `${shown} 외 ${hidden}` : shown;
}

function keywordSummaryParts(values = [], limit = 2, empty = "특이신호 없음") {
  const clean = uniqueValues(values).filter(Boolean);
  if (!clean.length) return { terms: [], empty };
  const hidden = clean.length - limit;
  return {
    terms: clean.slice(0, limit),
    extra: hidden > 0 ? `외 ${hidden}` : "",
    empty,
  };
}

function evidenceStrengthLabel(value) {
  const score = Number(value) || 0;
  if (score >= 85) return "근거 매우 강함";
  if (score >= 65) return "근거 충분";
  if (score >= 35) return "추가 확인";
  if (score > 0) return "근거 약함";
  return "근거 부족";
}

