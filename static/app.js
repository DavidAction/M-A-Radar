async function saveWorkflow(code) {
  const payload = {
    status: document.getElementById("workflowStatus")?.value || "미검토",
    contact_status: document.getElementById("workflowContact")?.value || "미접촉",
    owner: document.getElementById("workflowOwner")?.value || "",
    due_date: document.getElementById("workflowDue")?.value || "",
    next_action: document.getElementById("workflowNext")?.value || "",
    memo: document.getElementById("workflowMemo")?.value || "",
  };
  const stateEl = document.getElementById("workflowSaveState");
  if (stateEl) stateEl.textContent = "저장 중...";
  const res = await fetch(`/api/candidates/${encodeURIComponent(code)}/workflow`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(payload),
  });
  if (!res.ok) {
    if (stateEl) stateEl.textContent = "저장 실패";
    return;
  }
  const result = await res.json();
  state.items = state.items.map((item) =>
    item.code === code ? { ...item, workflow: result.workflow } : item,
  );
  renderList();
  renderDetail();
}

function bindWorkflowForm(item) {
  const button = document.getElementById("workflowSave");
  if (!button) return;
  button.addEventListener("click", () => {
    saveWorkflow(item.code);
  });
}

