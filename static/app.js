function signalFlagList(flags) {
  if (Array.isArray(flags)) return uniqueValues(flags);
  if (!flags || typeof flags !== "object") return [];
  return uniqueValues(
    Object.entries(flags).flatMap(([category, values]) => {
      const label = DEAL_SIGNAL_LABELS[category] || category;
      if (Array.isArray(values)) return values.map((value) => `${label}: ${value}`);
      return values ? [`${label}: ${values}`] : [];
    }),
  );
}

