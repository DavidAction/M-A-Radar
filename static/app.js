function normalizeSnippetStart(text = "", category = "") {
  let cleaned = String(text || "")
    .replace(/\s+/g, " ")
    .replace(/([가-힣A-Za-z])\1{3,}/g, "$1$1")
    .trim();
  cleaned = cleaned
    .replace(/^\d{1,4}(?:-\d+)+\)\s*/, "")
    .replace(/^[-–—·,\s]+/, "")
    .trim();

  const anchors = [
    ...(SNIPPET_ANCHORS[category] || []),
    "당사는",
    "회사는",
    "연결회사는",
    "연결실체는",
    "최대주주",
    "자본위험관리",
    "유상증자",
  ];
  for (const anchor of anchors) {
    const index = cleaned.indexOf(anchor);
    if (index > 0 && index < 260) {
      cleaned = cleaned.slice(index).trim();
      break;
    }
  }

  const completeStart =
    /^(당사는|회사는|연결회사는|연결실체는|최대주주|대표이사|정기주총|임시주총|자본위험관리|자본관리|계속기업|유상증자|전환사채|자금조달|사업의 개요|주요 제품|주요 매출처|특수관계자|감사의견|내부회계|20\d{2}[.년]|\d+[.)])/;
  if (cleaned && /^[가-힣]/.test(cleaned) && !completeStart.test(cleaned)) {
    cleaned = `...${cleaned}`;
  }
  if (cleaned && /^[a-z),.;:]/.test(cleaned)) {
    cleaned = `...${cleaned}`;
  }
  return normalizeEllipsis(cleaned);
}

function compactSnippet(text = "", maxLength = 280, category = "") {
  const cleaned = normalizeSnippetStart(text, category);
  if (cleaned.length <= maxLength) return cleaned;
  return appendEllipsis(cleaned.slice(0, maxLength));
}

function insightChips(values = [], tone = "") {
  const clean = values.filter(Boolean).slice(0, 6);
  if (!clean.length) return "";
  return `<div class="insight-chips">${clean.map((value) => `<span class="${tone}">${escapeHtml(value)}</span>`).join("")}</div>`;
}

function uniqueValues(values = []) {
  const seen = new Set();
  return (values || []).filter((value) => {
    const key = String(value || "").trim();
    if (!key || seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function compactSnippetItems(values = [], category = "", limit = 4) {
  const seen = new Set();
  return (values || [])
    .map((text) => compactSnippet(text, 280, category))
    .filter((text) => {
      const key = text.slice(0, 180);
      if (!key || seen.has(key)) return false;
      seen.add(key);
      return true;
    })
    .slice(0, limit);
}

