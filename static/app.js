function shortlistGroups() {
  const rows = state.shortlist?.items || [];
  if (!rows.length) return Object.entries(state.shortlist?.groups || {});
  const grouped = {};
  rows.forEach((row) => {
    const group = row.group || T.monitoring;
    if (!grouped[group]) grouped[group] = [];
    grouped[group].push(row);
  });
  const ordered = [
    ...GROUP_ORDER.filter((group) => grouped[group]),
    ...Object.keys(grouped).filter((group) => !GROUP_ORDER.includes(group)).sort(),
  ];
  return ordered.map((group) => [group, grouped[group]]);
}

